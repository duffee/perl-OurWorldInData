has query => (
    is  => 'rw',
    isa => Str,
);

# list of all the known charts
# should make it a constant
my @data = <DATA>;
chomp @data;

=head1 NAME

WebService::OurWorldInData::Catalog

=head1 SYNOPSIS

    my $catalog = WebService::OurWorldInData::Catalog->new;

    my $results = $chart->find( 'covid' ); # get charts for covid
    my @topics  = $chart->get_topics;

=head1 DESCRIPTION

Pretends to search Our World in Data for data charts like the python api C<find>.

=head1 Methods

=head2 find

Returns an array ref of charts matching the argument to C<find>.
You can give it a string or a regex as in

    $results = $chart->find( qr/covid|death/ );

An empty call to C<find> will return all the charts it knows about.

    $all_results = $chart->find;

Our World in Data holds the data on 14,368 charts across 123 topics
as of July 2025. Because they haven't opened up their Catalog API yet,
I've had to scrape their web page for results. This means that the
C<find> method is not dynamic, up-to-date or anywhere near complete.

There are currently 3659 charts known by this module,
so you've got a 1 in 4 chance of finding what you're looking for.

=head2 get_topics

This will return a list of topics that I've scraped so far.
There are only 29 of the 123 known as of July 2025, but it should
give you a start.

=head1 SEE ALSO

=over 4

=item * L<HTTP::Tiny>

=back

=cut

sub get_path {
    my $self = shift;
    return join '/', $self->base_url, 'data';
}

sub find {
    my ($self, $query) = @_;

    $query = qr// unless defined $query;
    $query = qr/$query/ unless ref $query eq 'Regexp';
    my @hits = grep { /$query/ } @data;
    return \@hits;

    # if we ever get an api to the data endpoint
    #return $self->get_response( $url, { q => $query } );
}

